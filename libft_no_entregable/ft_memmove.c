void	*ft_memmove(void *dest, const void *src, unsigned int n)
{
	char temp[n];
	int		i;

	i = 0;
	while (i < n)
	{
		temp[i] = ((char *)src)[i];
		i++;
	}
	i = 0;
	while (i < n)
	{
		((char *)dest)[i] = temp[i];
		i++;
	}
	return (dest);
}