#include "libft.h"

void	ft_lstadd_front(t_list **lst, t_list *new)
{
	new->next = *lst;
	*lst = new;
}

/* 
	lst es la dirección de memoria al primer nodo de la lista
	new es el nuevo puntero que añadir a la lista
 */